package main

import "fmt"

func main() {

	result := 10 + 10
	fmt.Println(result)

	a := 10
	b := 10
	c := a * b
	fmt.Println(c)

	i := 10
	i += 5 // Augmented Assignment
	fmt.Println(i)

	i++ // i = i + 1
	fmt.Println(i)

	i-- // i = i - 1
	fmt.Println(i)

	var negative = -100
	var positive = 100
	fmt.Println(negative)
	fmt.Println(positive)

}
